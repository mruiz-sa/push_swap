int	max_min_values(int *stack_a, int *stack_a_nbrs)
{
	int	i;

	i = 0;
	while (i < *stack_a_nbrs)
	{
		if (stack_a[i] < -2147483648 || stack_a[i] > 2147483647)
			return (1);
		i++;
	}
	return (0);
}
